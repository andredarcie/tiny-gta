// Gym bench press: p=1 lockout (arms extended), p=0 bar at chest.
export function applyBench(root: THREE.Object3D, p: number): void {
  applyBlend(root, BENCH_CHEST, BENCH_LOCKOUT, p < 0 ? 0 : p > 1 ? 1 : p, true);
}
const DANCE_LANES: Pose[] = [DANCE_LEFT, DANCE_DOWN, DANCE_UP, DANCE_RIGHT]; // matches dance-game lane ids 0..3
// Dance: blend the neutral groove toward the hit pose for `lane` by `amt` (0..1).
export function applyDance(root: THREE.Object3D, lane: number, amt: number): void {
  const target = DANCE_LANES[lane] ?? DANCE_NEUTRAL;
  applyBlend(root, DANCE_NEUTRAL, target, amt < 0 ? 0 : amt > 1 ? 1 : amt, true);
}
// Swimming: blend tread<->prone by `moving` (0 floating, 1 crawling forward), then add a
// gentle alternating flutter kick + arm scull so it reads as swimming, not a frozen pose.
// `t` is the stroke phase (radians) so the motion matches the swim cadence/splashes.
export function applySwim(root: THREE.Object3D, moving: number, t: number): void {
  applyBlend(root, SWIM_TREAD, SWIM_PRONE, moving < 0 ? 0 : moving > 1 ? 1 : moving, false);
  const kick = Math.sin(t * 2) * 0.3, scull = Math.sin(t) * 0.22;
  nudgeBone(root, 'LowerLegL', kick, 0, 0);
  nudgeBone(root, 'LowerLegR', -kick, 0, 0);
  nudgeBone(root, 'UpperArmL', 0, 0, -scull);
  nudgeBone(root, 'UpperArmR', 0, 0, scull);
  root.updateMatrixWorld(true);
  reconnectFeet(boneMap(root));
}
// Roof-fall ragdoll: the loose pose with a fast loose jiggle on every limb.
export function applyRagdoll(root: THREE.Object3D, t: number): void {
  applyPose(root, RAGDOLL, { feet: true });
  const j = (k: number) => Math.sin(t * 13 + k) * 0.4;
  nudgeBone(root, 'LowerArmL', j(0), 0, 0); nudgeBone(root, 'LowerArmR', j(2), 0, 0);
  nudgeBone(root, 'LowerLegL', j(4), 0, 0); nudgeBone(root, 'LowerLegR', j(6), 0, 0);
  root.updateMatrixWorld(true);
}
// Taxi hail: overlay the raised right arm (head stays on the clip), hand swinging side to side.
export function applyWave(root: THREE.Object3D, t: number): void {
  applyPose(root, NPC_WAVE, { only: ['UpperArmR', 'LowerArmR'] });
  nudgeBone(root, 'UpperArmR', 0, 0, Math.sin(t * 7) * 0.22);
  root.updateMatrixWorld(true);
}
// Cutscene talk: overlay both arms gesturing; gentle when talking, settled when not.
export function applyTalk(root: THREE.Object3D, t: number, talking: boolean): void {
  applyPose(root, NPC_TALK, { only: ['UpperArmL', 'LowerArmL', 'UpperArmR', 'LowerArmR'] });
  if (talking) {
    const g = Math.sin(t * 2.6) * 0.35;
    nudgeBone(root, 'UpperArmL', g, 0, 0);
    nudgeBone(root, 'UpperArmR', -g, 0, 0);
  }
  root.updateMatrixWorld(true);
}
