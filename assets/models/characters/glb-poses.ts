// cached bone lookup per root (avoids re-traversing every frame)
const _maps = new WeakMap<THREE.Object3D, Map<string, THREE.Bone>>();
function boneMap(root: THREE.Object3D): Map<string, THREE.Bone> {
  let m = _maps.get(root);
  if (m) return m;
  m = new Map();
  root.traverse(o => { if ((o as THREE.Bone).isBone) m!.set(o.name, o as THREE.Bone); });
  _maps.set(root, m);
  return m;
}
const _fp = new THREE.Vector3();
// Re-attach each Foot (its own root-parented chain) onto its shin end, so the foot
// follows the posed leg instead of floating at the bind position. Same fix used by
// applyVehiclePose in player-glb.ts.
function reconnectFeet(bones: Map<string, THREE.Bone>): void {
  for (const s of ['L', 'R'] as const) {
    const end = bones.get('LowerLeg' + s + '_end'), foot = bones.get('Foot' + s);
    if (end && foot && foot.parent) { _fp.setFromMatrixPosition(end.matrixWorld); foot.parent.worldToLocal(_fp); foot.position.copy(_fp); foot.updateMatrixWorld(true); }
  }
}

// Apply a pose to the GLB. `only` overlays just those bones (rest keeps the clip);
// `feet` re-attaches the foot bones after a leg-bearing pose. Call AFTER mixer.update().
export function applyPose(root: THREE.Object3D, pose: Pose, opts?: { feet?: boolean; only?: readonly string[] }): void {
  const bones = boneMap(root);
  const names = opts?.only ?? Object.keys(pose);
  for (const name of names) {
    const r = pose[name]; if (!r) continue;
    const b = bones.get(name); if (b) b.rotation.set(r[0], r[1], r[2]);
  }
  root.updateMatrixWorld(true);
  if (opts?.feet) { reconnectFeet(bones); }
}

// Per-bone linear blend of two full poses (t: 0→a, 1→b), allocation-free. Used for the
// animated lifts/sways (bench, dance, swim) where two keyframes interpolate each frame.
const _ea: [number, number, number] = [0, 0, 0];
export function applyBlend(root: THREE.Object3D, a: Pose, b: Pose, t: number, feet = true): void {
  const bones = boneMap(root);
  for (const name in a) {
    const ra = a[name], rb = b[name] ?? ra;
    const bone = bones.get(name); if (!bone) continue;
    _ea[0] = ra[0] + (rb[0] - ra[0]) * t;
    _ea[1] = ra[1] + (rb[1] - ra[1]) * t;
    _ea[2] = ra[2] + (rb[2] - ra[2]) * t;
    bone.rotation.set(_ea[0], _ea[1], _ea[2]);
  }
  root.updateMatrixWorld(true);
  if (feet) reconnectFeet(bones);
}

// Add a small delta (radians) to one bone's rotation AFTER a pose was applied — the
// cheap "life" layer (swim flutter, wave swing, ragdoll jiggle). No allocation.
export function nudgeBone(root: THREE.Object3D, name: string, dx: number, dy: number, dz: number): void {
  const b = boneMap(root).get(name);
  if (b) { b.rotation.x += dx; b.rotation.y += dy; b.rotation.z += dz; }
}

